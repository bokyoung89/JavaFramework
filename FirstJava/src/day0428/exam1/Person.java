package day0428.exam1;

public class Person {
	
	//Variable
	private String name;
	private int age;
		
	//Method
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
}
